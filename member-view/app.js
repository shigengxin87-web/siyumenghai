function selectDate(key) {
  const selected = parseDate(key);
  state.day = key;
  state.lens = 'all';
  if (state.view === 'members') state.view = 'overview';
  state.calendarMonth = new Date(selected.getFullYear(), selected.getMonth(), 1);
  render();
}

function renderCalendar() {
  const calendar = document.querySelector('[data-calendar]');
  const year = state.calendarMonth.getFullYear();
  const month = state.calendarMonth.getMonth();
  const firstDay = new Date(year, month, 1);
  const mondayOffset = (firstDay.getDay() + 6) % 7;
  const gridStart = new Date(year, month, 1 - mondayOffset);

  document.querySelector('[data-month-label]').textContent = `${year} 年 ${month + 1} 月`;
  document.querySelector('[data-today-label]').textContent = `${today.getMonth() + 1} 月 ${today.getDate()} 日`;
  calendar.innerHTML = Array.from({ length: 42 }, (_, index) => {
    const date = new Date(gridStart);
    date.setDate(gridStart.getDate() + index);
    const key = dateKey(date);
    const classes = ['calendar-day'];
    if (date.getMonth() !== month) classes.push('is-outside');
    if (days[key]) classes.push('has-report');
    if (key === todayKey) classes.push('is-today');
    if (key === state.day) classes.push('is-selected');
    const reportHint = days[key] ? '，已有日报' : '，暂无日报';
    return `<button type="button" class="${classes.join(' ')}" data-calendar-day="${key}" role="gridcell" aria-label="${readableDate(key)}${reportHint}" aria-selected="${key === state.day}">${date.getDate()}</button>`;
  }).join('');

  calendar.querySelectorAll('[data-calendar-day]').forEach((button) => {
    button.addEventListener('click', () => selectDate(button.dataset.calendarDay));
  });
}

