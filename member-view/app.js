function setupAmbientParticles() {
  const canvas = document.querySelector('[data-ambient-particles]');
  const workspace = canvas?.parentElement;
  if (!canvas || !workspace || !canvas.getContext) return;

  const motionPreference = window.matchMedia('(prefers-reduced-motion: reduce)');
  const context = canvas.getContext('2d');
  let particles = [];
  let frame = 0;
  let width = 0;
  let height = 0;
  let pixelRatio = 1;

  function createParticle() {
    return {
      x: Math.random() * width,
      y: Math.random() * height,
      radius: .8 + Math.random() * 2.1,
      speedX: -.08 + Math.random() * .16,
      speedY: -.12 - Math.random() * .16,
      alpha: .08 + Math.random() * .16,
      phase: Math.random() * Math.PI * 2
    };
  }

  function resize() {
    const bounds = workspace.getBoundingClientRect();
    width = Math.max(1, bounds.width);
    height = Math.max(1, bounds.height);
    pixelRatio = Math.min(window.devicePixelRatio || 1, 2);
    canvas.width = Math.round(width * pixelRatio);
    canvas.height = Math.round(height * pixelRatio);
    context.setTransform(pixelRatio, 0, 0, pixelRatio, 0, 0);
    const particleCount = Math.max(16, Math.min(34, Math.round(width * height / 42000)));
    particles = Array.from({ length: particleCount }, createParticle);
  }

  function draw() {
    if (motionPreference.matches || document.hidden) {
      frame = 0;
      return;
    }
    context.clearRect(0, 0, width, height);
    particles.forEach((particle, index) => {
      particle.x += particle.speedX + Math.sin(particle.phase) * .025;
      particle.y += particle.speedY;
      particle.phase += .006;
      if (particle.y < -8) { particle.y = height + 8; particle.x = Math.random() * width; }
      if (particle.x < -8) particle.x = width + 8;
      if (particle.x > width + 8) particle.x = -8;

      const glow = particle.alpha * (.72 + Math.sin(particle.phase + index) * .28);
      context.beginPath();
      context.arc(particle.x, particle.y, particle.radius, 0, Math.PI * 2);
      context.fillStyle = `rgba(5, 150, 105, ${glow})`;
      context.fill();
    });
    frame = window.requestAnimationFrame(draw);
  }

  function updateMotion() {
    if (frame) window.cancelAnimationFrame(frame);
    frame = 0;
    context.clearRect(0, 0, width, height);
    if (!motionPreference.matches && !document.hidden) frame = window.requestAnimationFrame(draw);
  }

  resize();
  const resizeObserver = new ResizeObserver(resize);
  resizeObserver.observe(workspace);
  motionPreference.addEventListener?.('change', updateMotion);
  document.addEventListener('visibilitychange', updateMotion);
  updateMotion();
}

