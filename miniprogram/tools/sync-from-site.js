#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const miniRoot = path.resolve(__dirname, '..');
const siteRootArg = process.argv.indexOf('--site-root');
const siteRoot = siteRootArg >= 0
  ? path.resolve(process.argv[siteRootArg + 1])
  : path.resolve(miniRoot, '..');
const memberViewRoot = path.join(siteRoot, 'member-view');

function read(relativePath) {
  return fs.readFileSync(path.join(memberViewRoot, relativePath), 'utf8');
}

function normalizedLinks(item) {
  const candidates = Array.isArray(item.links)
    ? item.links
    : item.url
      ? [{ label: item.linkLabel || '查看完整内容', url: item.url }]
      : [];
  return candidates.filter((link) => /^https?:\/\//.test(link.url || ''));
}

function prepareItem(item) {
  const next = { ...item };
  const links = normalizedLinks(next);
  delete next.url;
  delete next.linkLabel;
  delete next.links;
  if (links.length) next.links = links;
  return next;
}

function prepareReport(key, report) {
  const [, month, day] = key.split('-').map(Number);
  return {
    ...report,
    dateLabel: `${month} 月 ${day} 日`,
    insights: (report.insights || []).map(prepareItem),
    actions: (report.actions || []).map(prepareItem),
    resources: (report.resources || []).map(prepareItem),
    themes: (report.themes || []).map(prepareItem),
  };
}

const context = vm.createContext({});
vm.runInContext(`${read('archive-days.js')}\nglobalThis.__archiveDays = archiveDays;`, context);
const appSource = read('app.js');
const membersMarker = appSource.indexOf('\nlet members =');
if (membersMarker < 0) throw new Error('Cannot locate the report boundary in member-view/app.js');
vm.runInContext(`${appSource.slice(0, membersMarker)}\nglobalThis.__days = days;`, context);

const reports = Object.fromEntries(
  Object.entries(JSON.parse(JSON.stringify(context.__days)))
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([key, report]) => [key, prepareReport(key, report)]),
);
const reportKeys = Object.keys(reports);
const reportsOutput = `// Generated by tools/sync-from-site.js. Do not edit manually.\nconst reports = ${JSON.stringify(reports, null, 2)};\n\nconst reportKeys = Object.keys(reports).sort();\n\nmodule.exports = { reports, reportKeys };\n`;
fs.writeFileSync(path.join(miniRoot, 'data', 'reports.js'), reportsOutput);

const memberPayload = JSON.parse(read('members.json'));
const members = memberPayload.members.map((member, index) => {
  const order = index + 1;
  const fileName = `member-${String(order).padStart(2, '0')}.jpg`;
  const sourceRelative = member.avatar.split('?')[0].replace(/^\.\//, '');
  fs.copyFileSync(path.join(memberViewRoot, sourceRelative), path.join(miniRoot, 'assets', 'members', fileName));
  return {
    name: member.name,
    order,
    number: String(order).padStart(2, '0'),
    avatar: `/assets/members/${fileName}`,
  };
});
const membersOutput = `// Generated by tools/sync-from-site.js. Do not edit manually.\nconst members = ${JSON.stringify(members, null, 2)};\n\nmodule.exports = { members };\n`;
fs.writeFileSync(path.join(miniRoot, 'data', 'members.js'), membersOutput);

console.log(JSON.stringify({
  status: 'synced',
  reports: reportKeys.length,
  firstDate: reportKeys[0],
  lastDate: reportKeys[reportKeys.length - 1],
  members: members.length,
  membersUpdatedAt: memberPayload.updatedAt,
}, null, 2));
